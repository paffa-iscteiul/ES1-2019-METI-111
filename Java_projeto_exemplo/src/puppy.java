
public class puppy {

	private int age;
	private String name;
	private String owner;
	private int id;
	
	public puppy(int age, String name, String owner, int id) {
		this.age = age;
		this.name = name;
		this.owner = owner;
		this.id = id;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getOwner() {
		return owner;
	}

	public void setOwner(String owner) {
		this.owner = owner;
	}
	
	
}
